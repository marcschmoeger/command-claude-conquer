export const getSkillById = (id: string): Skill | undefined => {
  return skills.find((s) => s.id === id);
};

export const getSkillsByCategory = (category: string): Skill[] => {
  return skills.filter((s) => s.category === category);
};

export const getAvailableSkills = (userLevel: number, connectedMcps: string[]): Skill[] => {
  return skills.filter((skill) => {
    if (skill.requiredLevel > userLevel) return false;
    if (skill.requiredMcp.length > 0) {
      return skill.requiredMcp.some((mcp) => connectedMcps.includes(mcp));
    }
    return true;
  });
};
